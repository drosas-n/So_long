#include "ft_printf.h"

int	ft_decsig(int nb)
{
	int	a;
	int	x;

	x = 0;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
		x = 1;
	}
	if (nb >= 10)
		x += ft_decsig(nb / 10);
	a = nb % 10 + '0';
	write(1, &a, 1);
	x++;
	return (x);
}
